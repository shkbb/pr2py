# Запитуємо у користувача трицифрове число
number = int(input("Введіть трицифрове число: "))

# Отримуємо першу цифру числа (розподіл націло на 100)
first_digit = number // 100

# Отримуємо останню цифру числа (остача від ділення на 10)
last_digit = number % 10

# Перевіряємо, чи рівні перша і остання цифри
if first_digit == last_digit:
    print("Число є паліндромом")
else:
    print("Число не є паліндромом")
